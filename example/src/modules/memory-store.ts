// **** TYPINGS **** //

type MemoryStore = {
    [type: string]: any;
};

// **** DEFINITIONS **** //

const store: MemoryStore = {};

// **** PUBLIC METHODS **** //

export const getFromStore = <T>(key: string) => {
    console.info(`Getting "${key}" from store...`);
    const k = store[key];
    return k !== void 0 ? (k as T) : null;
};

export const setToStore = (key: string, payload: any) => {
    console.info(`Setting "${key}" to store...`);
    store[key] = payload;
    return store;
};
